#include <unistd.h>

int main(int argc, char **argv)
{
    int i = 0;
    int j = 0;
    
    if(argc == 2)
    {
        while (argv[1][i] == 32 || argv[1][i] == '\t')
			i++;
        while(argv[1][i] != '\0')
        {
            while(argv[1][i] >= 33 && argv[1][i] <= 126)
            {
                write(1, &argv[1][i], 1);
                i++;
            }
            if ((argv[1][i] == 32 || argv[1][i] == '\t') && (argv[1][i + 1] >= 33 && argv[1][i + 1] <= 126))
            {
                while(j < 3)
                {
                    write(1, " ", 1);
                    j++;
                }
            }
            j = 0;
            i++;
        }
    }
    write(1, "\n", 1);
    return (0);
}